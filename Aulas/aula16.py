#operadores de comparação
'''
OP-------SIGNIFICADO--------EXEMPLO (TRUE)
>--------MAIOR--------------2>1
>=--------MAIOR OU IGUAL--------------2>=1
<--------MENOR--------------2<1
<=--------MENOR OU IGUAL--------------2<=1
==--------IGUAL--------------'a' == 'a'
!=--------DIFERENTE--------------'a' != 'b'
'''

maior = 2 > 1
maior_ou_igual = 2 >= 2
menor = 1 < 2
menor_ou_igual = 2 <= 2
igual = 'a' == 'a'
diferente = 'a' != 'b'

print(maior, maior_ou_igual, menor, menor_ou_igual, igual, diferente)
