# Tipo de dado bool (boolean)
# Ao questionar algo só existem duas respostas possiveis:
# sim (True) ou não(False)
# Existem vários operadores para "questionas"
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual a outro

print(10 == 10)
print(10 == -10)
print(10 == -1)
print(10 == 1)
print(type(True))